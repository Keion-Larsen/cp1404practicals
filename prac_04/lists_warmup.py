numbers = [3, 1, 4, 1, 5, 9, 2]

#number[0] = 3
print(numbers[0])

#number[-1] = 2
print(numbers[-1])

#numbers[3] = 1
print(numbers[3])

#numbers[:-1] = 1
print(numbers[:-1])

#numbers[3:4] = 1
print(numbers[3:4])

# 5 in numbers = True
print(5 in numbers)

#7 in numbers = False
print(7 in numbers)

#"3" in numbers = False
print("3" in numbers)

#numbers + [6, 5, 3] = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3]
print(numbers + [6, 5, 3])


